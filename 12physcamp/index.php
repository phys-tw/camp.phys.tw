    <p>台大物理營是台大物理系學生會例行舉辦之冬令營隊活動，民國八十七年首辦以來，即將邁入第十五個年頭。一般高中生對於「物理」這們科學，常畏於其眾多的公式與繁雜的計算，殊不知物理的內涵，遠非一般高中教育所能述盡；而體制與課本，卻常常扼殺了學生的思考與創造力。本營隊興辦的意圖，即是欲藉正規物理教育以外的手段，包括生動的演講、新奇的實驗、深度的博物館參觀等，引領學生揭開物理神秘之面紗，體會科學之美。</p>
