    <h1>關於台大物理營</h1>
    <p>台大物理營是台大物理系學生會於每年寒假時會例行舉辦之冬令營隊活動，自民國八十七年首辦以來，至今已經歷過許多的年頭。</P>
    <p>「物理」這門科學，對於高中生來說常建立於其眾多的公式與繁雜的計算，殊不知物理的內涵，遠非一般高中教育所能述盡。我們將透過<strong>精心規劃的活動、豐富生動的演講</strong>、<strong>新奇趣味的實驗</strong>、<strong>深度的實驗室參觀</strong>等，引領學生揭開物理神秘的面紗，體會科學之美，並在單調的高中生活之餘放鬆心情、展現自我、增進人際關係。</p>
    <p>在歷屆的台大物理營中，我們成功地向高中生介紹現今物理各領域的蓬勃發展，展現物理學的實際應用面，並藉此讓學生重新認識自我。我們的努力成效卓著，台大物理營在全國高中生中已成為口耳相傳的熱門營隊。</p>
